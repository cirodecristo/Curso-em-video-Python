x = input('Digite o que você quiser:')
print('O tipo primitivo desse valor é:', type(x))
print('É uma letra?', x.isalpha())
print('É um alfanumérico?', x.isalnum())
print('Está somente em minúsculas:', x.islower())
print('É um decimal:', x.isdecimal())
print('Está somente em maiúsculas:', x.isupper())
print('Contém somente espaços?', x.isspace())
print('Está capitalizada?', x.istitle())


